<?php 
//檔案：api/find_pw.php
//功能：提供「忘記密碼」查詢功能，透過電子郵件信箱尋找帳號並回傳對應密碼。

// 包含資料庫設定檔以存取 $Mem 物件
include_once "db.php";

// 1. 查詢資料：根據前端傳入的 email 參數，在會員資料表中尋找對應的記錄
$user = $Mem->find(['email' => $_GET['email']]);

// 2. 狀態判斷：若查詢結果不為空，顯示密碼；否則提示查無資料
if(!empty($user)){
    echo "您的密碼為:" . $user['pw'];
}else{
    echo "查無此資料";
}

//這段程式碼實現了簡單的密碼取回機制。在實際的商業網站應用中，通常不會直接回傳明碼密碼
//（因為資料庫應該儲存雜湊後的密碼），而是會寄送一封包含「重設密碼連結」的電子郵件給使用者。
